CREATE TABLE ODS.PLANET_DIM (
  PLANET_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NAME             VARCHAR2(100) NOT NULL,
  TYPE             VARCHAR2(100),
  PRIMARY_RESOURCE VARCHAR2(100),
  SECONDARY_RESOURCE VARCHAR2(100),
  ATMOSPHERE       VARCHAR2(500),
  DIFFICULTY_LEVEL NUMBER,   -- 1=Facile, 2=Moyenne, 3=Difficile, 4=Très difficile
  SOLAR_POWER      NUMBER,   -- 1=Très faible, 2=Faible, 3=Moyen, 4=Fort, 5=Très élevé
  WIND_POWER       NUMBER,   -- 1=Très faible, 2=Faible, 3=Moyen, 4=Fort, 5=Très élevé
  CORE_SYMBOL_URL  VARCHAR2(500),
  CORE_MATERIAL    VARCHAR2(100),
  SOURCE_LOG_ID    NUMBER     
);